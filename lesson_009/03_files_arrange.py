# Нужно написать скрипт для упорядочивания фотографий (вообще любых файлов)
# Скрипт должен разложить файлы из одной папки по годам и месяцам в другую.
# Например, так:
#   исходная папка
#       icons/cat.jpg
#       icons/man.jpg
#       icons/new_year_01.jpg
#   результирующая папка
#       icons_by_year/2018/05/cat.jpg
#       icons_by_year/2018/05/man.jpg
#       icons_by_year/2017/12/new_year_01.jpg
#
# Входные параметры основной функции: папка для сканирования, целевая папка.
# Имена файлов в процессе работы скрипта не менять, год и месяц взять из времени последней модификации файла
# (время создания файла берется по разному в разых ОС - см https://clck.ru/PBCAX - поэтому берем время модификации).
#
# Файлы для работы взять из архива icons.zip - раззиповать проводником ОС в папку icons перед написанием кода.
# Имя целевой папки - icons_by_year (тогда она не попадет в коммит, см .gitignore в папке ДЗ)
#
# Пригодятся функции:
#   os.walk
#   os.path.dirname
#   os.path.join
#   os.path.normpath
#   os.path.getmtime
#   time.gmtime
#   os.makedirs
#   shutil.copy2
#
# Чтение документации/гугла по функциям - приветствуется. Как и поиск альтернативных вариантов :)
#
# Требования к коду: он должен быть готовым к расширению функциональности - делать сразу на классах.
# Для этого пригодится шаблон проектирование "Шаблонный метод"
#   см https://refactoring.guru/ru/design-patterns/template-method
#   и https://gitlab.skillbox.ru/vadim_shandrinov/python_base_snippets/snippets/4

folder = []
file_open = os.walk(r'E:\Users\Desktop\Vito\Python\python_base\icons')


def working_file():
    for address, dirs, files in file_open:
        folder.append(files)
        for file in files:
            create_time = os.path.getmtime(address + '/' + file)
            print_datetime = datetime.fromtimestamp(create_time)
            print(f'|{address}\{file} {"":-^15}{">"} {print_datetime} ')


def create_folder():
    workspace = os.getcwd()
    folders = r'icons_by_year'
    way = os.path.join(workspace + os.sep + folders)
    for path in range(1, 13):
        if path > 9:
            if not os.path.exists(str(way)):
                os.makedirs(str(way))
                print("create folder with path {0}".format(way))
            else:
                print("folder exists {0}".format(way))


create_folder()

